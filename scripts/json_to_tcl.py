#!/usr/bin/env python3
import argparse
import json
from pathlib import Path

def _constraints_to_fix(ndf, constraints):
    if constraints is None:
        return None
    if isinstance(constraints, list):
        if all(isinstance(v, bool) for v in constraints):
            if len(constraints) != ndf:
                raise ValueError(f"constraints bool list length {len(constraints)} != ndf {ndf}")
            return [1 if v else 0 for v in constraints]
        # assume list of 1-based dof indices
        fix = [0] * ndf
        for dof in constraints:
            if not isinstance(dof, int):
                raise ValueError("constraints must be list of ints or bools")
            if dof < 1 or dof > ndf:
                raise ValueError(f"constraint dof {dof} out of range 1..{ndf}")
            fix[dof - 1] = 1
        return fix
    raise ValueError("constraints must be list")


def _write_header(f):
    f.write("# Auto-generated by json_to_tcl.py\n")


def _require_field(obj, key):
    if key not in obj:
        raise ValueError(f"missing required field: {key}")
    return obj[key]


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("json_file")
    parser.add_argument("tcl_out")
    args = parser.parse_args()

    data = json.loads(Path(args.json_file).read_text())
    schema_version = data.get("schema_version", "1.0")
    if schema_version != "1.0":
        raise ValueError(f"unsupported schema_version: {schema_version}")

    model = _require_field(data, "model")
    ndm = _require_field(model, "ndm")
    ndf = _require_field(model, "ndf")

    nodes = _require_field(data, "nodes")
    elements = _require_field(data, "elements")
    sections = {sec["id"]: sec for sec in data.get("sections", [])}
    materials = {mat["id"]: mat for mat in data.get("materials", [])}

    recorders = data.get("recorders", [])

    tcl_path = Path(args.tcl_out)
    tcl_path.parent.mkdir(parents=True, exist_ok=True)

    with tcl_path.open("w", encoding="utf-8") as f:
        _write_header(f)
        f.write(f"model BasicBuilder -ndm {ndm} -ndf {ndf}\n")

        for node in nodes:
            node_id = node["id"]
            if ndm == 2:
                f.write(f"node {node_id} {node['x']} {node['y']}\n")
            elif ndm == 3:
                f.write(f"node {node_id} {node['x']} {node['y']} {node['z']}\n")
            else:
                raise ValueError("ndm must be 2 or 3")

            fix = _constraints_to_fix(ndf, node.get("constraints"))
            if fix is not None:
                f.write(f"fix {node_id} {' '.join(str(v) for v in fix)}\n")

        # Materials (Elastic uniaxial or ElasticIsotropic)
        for mat in materials.values():
            params = mat["params"]
            if mat["type"] == "Elastic":
                E = params["E"]
                f.write(f"uniaxialMaterial Elastic {mat['id']} {E}\n")
            elif mat["type"] == "ElasticIsotropic":
                E = params["E"]
                nu = params["nu"]
                rho = params.get("rho", 0.0)
                f.write(f"nDMaterial ElasticIsotropic {mat['id']} {E} {nu} {rho}\n")
            else:
                raise ValueError(f"unsupported material type: {mat['type']}")

        # Sections (ElasticSection2d or ElasticMembranePlateSection)
        for sec in sections.values():
            params = sec["params"]
            if sec["type"] == "ElasticSection2d":
                E = params["E"]
                A = params["A"]
                I = params["I"]
                f.write(f"section Elastic {sec['id']} {E} {A} {I}\n")
            elif sec["type"] == "ElasticMembranePlateSection":
                E = params["E"]
                nu = params["nu"]
                h = params["h"]
                rho = params.get("rho", 0.0)
                f.write(
                    f"section ElasticMembranePlateSection {sec['id']} {E} {nu} {h} {rho}\n"
                )
            else:
                raise ValueError(f"unsupported section type: {sec['type']}")

        # Geometric transformations (only for beam-column elements)
        transf_tags = {}
        next_tag = 1
        for elem in elements:
            if elem["type"] != "elasticBeamColumn2d":
                continue
            name = elem.get("geomTransf", "Linear")
            if name not in transf_tags:
                transf_tags[name] = next_tag
                f.write(f"geomTransf {name} {next_tag}\n")
                next_tag += 1

        # Elements (elasticBeamColumn2d, truss, zeroLength, twoNodeLink, fourNodeQuad, shell)
        for elem in elements:
            if elem["type"] == "elasticBeamColumn2d":
                sec = sections[elem["section"]]
                params = sec["params"]
                A = params["A"]
                E = params["E"]
                I = params["I"]
                n1, n2 = elem["nodes"]
                transf_tag = transf_tags[elem.get("geomTransf", "Linear")]
                f.write(
                    f"element elasticBeamColumn {elem['id']} {n1} {n2} {A} {E} {I} {transf_tag}\n"
                )
            elif elem["type"] == "truss":
                n1, n2 = elem["nodes"]
                area = elem["area"]
                mat_id = elem["material"]
                f.write(f"element truss {elem['id']} {n1} {n2} {area} {mat_id}\n")
            elif elem["type"] == "zeroLength":
                n1, n2 = elem["nodes"]
                mats = " ".join(str(mid) for mid in elem["materials"])
                dirs = " ".join(str(d) for d in elem["dirs"])
                f.write(f"element zeroLength {elem['id']} {n1} {n2} -mat {mats} -dir {dirs}\n")
            elif elem["type"] == "twoNodeLink":
                n1, n2 = elem["nodes"]
                mats = " ".join(str(mid) for mid in elem["materials"])
                dirs = " ".join(str(d) for d in elem["dirs"])
                f.write(f"element twoNodeLink {elem['id']} {n1} {n2} -mat {mats} -dir {dirs}\n")
            elif elem["type"] == "fourNodeQuad":
                n1, n2, n3, n4 = elem["nodes"]
                t = elem["thickness"]
                mat_id = elem["material"]
                formulation = elem.get("formulation", "PlaneStress")
                f.write(
                    f"element quad {elem['id']} {n1} {n2} {n3} {n4} {t} \"{formulation}\" {mat_id}\n"
                )
            elif elem["type"] == "shell":
                n1, n2, n3, n4 = elem["nodes"]
                sec_id = elem["section"]
                f.write(f"element shell {elem['id']} {n1} {n2} {n3} {n4} {sec_id}\n")
            else:
                raise ValueError(f"unsupported element type: {elem['type']}")

        # Loads
        loads = data.get("loads", [])
        if loads:
            f.write("timeSeries Linear 1\n")
            f.write("pattern Plain 1 1 {\n")
            load_map = {}
            for load in loads:
                node_id = load["node"]
                vec = load_map.setdefault(node_id, [0.0] * ndf)
                dof = load["dof"]
                if dof < 1 or dof > ndf:
                    raise ValueError(f"load dof {dof} out of range 1..{ndf}")
                vec[dof - 1] += load["value"]
            for node_id, vec in load_map.items():
                f.write(f"  load {node_id} {' '.join(str(v) for v in vec)}\n")
            f.write("}\n")

        # Analysis setup
        analysis = data.get("analysis", {"type": "static_linear", "steps": 1})
        if analysis["type"] != "static_linear":
            raise ValueError(f"unsupported analysis type: {analysis['type']}")
        steps = analysis.get("steps", 1)
        f.write("constraints Plain\n")
        f.write("numberer RCM\n")
        f.write("system BandGeneral\n")
        f.write("test NormUnbalance 1.0e-12 10\n")
        f.write("algorithm Linear\n")
        f.write("integrator LoadControl 1.0\n")
        f.write("analysis Static\n")

        # Recorders
        for rec in recorders:
            if rec["type"] != "node_displacement":
                raise ValueError(f"unsupported recorder type: {rec['type']}")
            dofs = rec["dofs"]
            output = rec.get("output", "node_disp")
            for node_id in rec["nodes"]:
                filename = f"{output}_node{node_id}.out"
                f.write(
                    f"recorder Node -file {filename} -node {node_id} -dof {' '.join(str(d) for d in dofs)} disp\n"
                )

        f.write(f"analyze {steps}\n")


if __name__ == "__main__":
    main()
